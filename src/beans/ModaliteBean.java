package beans;

public class ModaliteBean {
	
	private String aides, conditions;
	
	public ModaliteBean(){
		aides = "null";
		conditions = "null";
	}

	public String getAides() {
		return aides;
	}

	public void setAides(String aides) {
		this.aides = aides;
	}

	public String getConditions() {
		return conditions;
	}

	public void setConditions(String conditions) {
		this.conditions = conditions;
	}

}
